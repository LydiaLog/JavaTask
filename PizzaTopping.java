public class PizzaTopping {
    public static void main(String[] args) {

        String[] toppings = { "Pepperoni", "Mushrooms", "Onions", "Sausage", "Bacon" }; // 문자열을 포함한 배열 생성

        for (int i = 0; i < toppings.length; i++) {
		  
            System.out.print(toppings[i] + " ");
      }
   }
}